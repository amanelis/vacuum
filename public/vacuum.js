  var request = vacuum.createXMLHTTPObject();
  request.open('POST', vacuum.api_url + vacuum.log_path + '?api_key=' + vacuum.api_key + params, true);
	request.setRequestHeader('Content-type','application/x-www-form-urlencoded');
